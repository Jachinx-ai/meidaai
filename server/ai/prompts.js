/* 模型2 第2步 · 单品平铺图生成（逐件调用） */
function flatImagePrompt(category, description) {
  return `参考图片中的【${category}】（${description}），生成这件服装的电商商品平铺图。

要求：
- 只画这一件服装，不要出现人物、其他服装或任何配饰
- 服装平整展开、正面视角、居中构图，完整展示袖长/裤长/裙长/鞋型
- 上衣展开肩线和袖子；下装完整展示腰头和裤脚/裙摆；鞋子生成一双并排商品图
- 纯白色背景，光照均匀柔和，无明显阴影、无文字、无水印、无衣架、无模特
- 忠实还原原图中这件服装的主色、款式、材质、图案、logo位置，不要自行改设计
- 如果参考图里服装被人体遮挡，只补全合理结构，不要添加新的花纹或装饰`;
}

